package java10;
public class College {  
		private int id;
		private String Name;
		private long PhNo;
		public void setId(int id) {
			this.id = id;
		}
		public void setName(String name) {
			this.Name = name;
		}
		public void setPhNo(long phNo) {
			this.PhNo = phNo;
		}
		public int getId() {
			return id;
		}
		public String getName() {
			return Name;
		}
		public long getPhNo() {
			return PhNo;
		}
}
		
		
			
